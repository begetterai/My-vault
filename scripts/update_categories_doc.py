def get_session():
    creds = service_account.Credentials.from_service_account_file(
        CREDS, scopes=['https://www.googleapis.com/auth/documents'])
    return AuthorizedSession(creds)

def clear_and_write(s, doc_id, text):
    # Узнаём длину
    r = s.get(f'https://docs.googleapis.com/v1/documents/{doc_id}')
    doc = r.json()
    end_idx = doc['body']['content'][-1]['endIndex']

    reqs = []
    # Удаляем всё кроме последнего \n
    if end_idx > 2:
        reqs.append({
            'deleteContentRange': {
                'range': {'startIndex': 1, 'endIndex': end_idx - 1}
            }
        })
    # Вставляем новый текст
    reqs.append({'insertText': {'location': {'index': 1}, 'text': text}})

    r2 = s.post(
        f'https://docs.googleapis.com/v1/documents/{doc_id}:batchUpdate',
        json={'requests': reqs}, timeout=30)
    return r2.status_code

def apply_styles(s, doc_id):
    r = s.get(f'https://docs.googleapis.com/v1/documents/{doc_id}')
    doc = r.json()
    body = doc['body']['content']
    end_idx = body[-1]['endIndex'] - 1

    reqs = [
        # Поля
        {
            'updateDocumentStyle': {
                'documentStyle': {
                    'pageSize': {
                        'width':  {'magnitude': 595.28, 'unit': 'PT'},
                        'height': {'magnitude': 841.89, 'unit': 'PT'},
                    },
                    'marginTop':    {'magnitude': 56.70, 'unit': 'PT'},
                    'marginBottom': {'magnitude': 56.70, 'unit': 'PT'},
                    'marginLeft':   {'magnitude': 85.05, 'unit': 'PT'},
                    'marginRight':  {'magnitude': 42.52, 'unit': 'PT'},
                },
                'fields': 'pageSize,marginTop,marginBottom,marginLeft,marginRight'
            }
        },
        # Весь текст — Times New Roman 11pt
        {
            'updateTextStyle': {
                'range': {'startIndex': 1, 'endIndex': end_idx},
                'textStyle': {
                    'weightedFontFamily': {'fontFamily': 'Times New Roman'},
                    'fontSize': {'magnitude': 11, 'unit': 'PT'},
                    'bold': False,
                },
                'fields': 'weightedFontFamily,fontSize,bold'
            }
        },
        # Межстрочный
        {
            'updateParagraphStyle': {
                'range': {'startIndex': 1, 'endIndex': end_idx},
                'paragraphStyle': {'lineSpacing': 115},
                'fields': 'lineSpacing'
            }
        },
    ]

    # Заголовки
    for elem in body:
        if 'paragraph' not in elem:
            continue
        para = elem['paragraph']
        text = ''.join(
            e.get('textRun', {}).get('content', '')
            for e in para.get('elements', [])
        ).strip()

        is_main_title = text.startswith('ИНСТРУКЦИЯ —')
        is_block = text.startswith('БЛОК ')
        is_separator = text.startswith('═') or text.startswith('─')
        is_cat_name = (
            not text.startswith(' ') and
            not text.startswith('✗') and
            not text.startswith('✅') and
            not text.startswith('⚠') and
            not text.startswith('•') and
            not text.startswith('Что') and
            not text.startswith('Пример') and
            not text.startswith('Граница') and
            not text.startswith('ГРАНИЦА') and
            not text.startswith('Фото') and
            not text.startswith('Идёт') and
            not text.startswith('Исполь') and
            not is_separator and
            not is_main_title and
            not is_block and
            len(text) > 3
        )

        si, ei = elem['startIndex'], elem['endIndex']

        if is_main_title:
            reqs += [
                {'updateTextStyle': {
                    'range': {'startIndex': si, 'endIndex': ei - 1},
                    'textStyle': {'bold': True, 'fontSize': {'magnitude': 14, 'unit': 'PT'}},
                    'fields': 'bold,fontSize'
                }},
                {'updateParagraphStyle': {
                    'range': {'startIndex': si, 'endIndex': ei},
                    'paragraphStyle': {'alignment': 'CENTER'},
                    'fields': 'alignment'
                }},
            ]
        elif is_block:
            reqs += [
                {'updateTextStyle': {
                    'range': {'startIndex': si, 'endIndex': ei - 1},
                    'textStyle': {'bold': True, 'fontSize': {'magnitude': 12, 'unit': 'PT'}},
                    'fields': 'bold,fontSize'
                }},
            ]

    r2 = s.post(
        f'https://docs.googleapis.com/v1/documents/{doc_id}:batchUpdate',
        json={'requests': reqs}, timeout=30)
    return r2.status_code

