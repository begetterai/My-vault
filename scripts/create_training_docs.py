def get_session():
    creds = service_account.Credentials.from_service_account_file(
        CREDS, scopes=['https://www.googleapis.com/auth/drive',
                       'https://www.googleapis.com/auth/documents'])
    return AuthorizedSession(creds)


def create_doc(s, title, content):
    r = s.post(
        'https://www.googleapis.com/drive/v3/files?supportsAllDrives=true',
        headers={'Content-Type': 'application/json'},
        data=json.dumps({
            'name': title,
            'mimeType': 'application/vnd.google-apps.document',
            'parents': [FOLDER_ID],
        }), timeout=30)
    doc_id = r.json()['id']

    r2 = s.post(
        f'https://docs.googleapis.com/v1/documents/{doc_id}:batchUpdate',
        headers={'Content-Type': 'application/json'},
        data=json.dumps({'requests': [
            {'insertText': {'location': {'index': 1}, 'text': content}}
        ]}), timeout=30)

    if r2.status_code != 200:
        print(f'  ❌ Вставка текста провалилась: {r2.text[:200]}')
        return None

    ok = apply_doc_styles(s, doc_id)
    print(f'  {"✅" if ok else "⚠️  стили"} {title}')
    print(f'  https://docs.google.com/document/d/{doc_id}/edit')
    return doc_id


def main():
    s = get_session()
    print('Создаю тренинги...\n')
    create_doc(s, 'Ромашка — Тренинг по списаниям (V.1)', SPISANIYA)
    print()
    create_doc(s, 'Ромашка — Тренинг по инвентаризации (V.1)', INVENTARIZACIYA)


if __name__ == '__main__':
    main()
