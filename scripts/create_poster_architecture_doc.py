def get_session():
    scopes = [
        'https://www.googleapis.com/auth/drive',
        'https://www.googleapis.com/auth/documents',
    ]
    creds = service_account.Credentials.from_service_account_file(CREDS, scopes=scopes)
    return AuthorizedSession(creds)

def create_doc_in_drive(s, title):
    body = {
        'name': title,
        'mimeType': 'application/vnd.google-apps.document',
        'parents': [DRIVE_ID],
    }
    r = s.post(
        'https://www.googleapis.com/drive/v3/files',
        params={'supportsAllDrives': 'true'},
        json=body, timeout=30
    )
    if r.status_code not in (200, 201):
        raise RuntimeError(f"Drive create failed: {r.status_code} {r.text[:300]}")
    return r.json()['id']

def insert_text(s, doc_id, text):
    requests = [{'insertText': {'location': {'index': 1}, 'text': text}}]
    r = s.post(
        f'https://docs.googleapis.com/v1/documents/{doc_id}:batchUpdate',
        json={'requests': requests}, timeout=30
    )
    return r.status_code == 200

def apply_styles(s, doc_id):
    r = s.get(f'https://docs.googleapis.com/v1/documents/{doc_id}', timeout=20)
    doc = r.json()
    body_content = doc['body']['content']
    end_idx = body_content[-1]['endIndex'] - 1

    reqs = [
        # Поля страницы
        {
            'updateDocumentStyle': {
                'documentStyle': {
                    'pageSize': {
                        'width':  {'magnitude': 595.28, 'unit': 'PT'},
                        'height': {'magnitude': 841.89, 'unit': 'PT'},
                    },
                    'marginTop':    {'magnitude': 56.70, 'unit': 'PT'},
                    'marginBottom': {'magnitude': 56.70, 'unit': 'PT'},
                    'marginLeft':   {'magnitude': 85.05, 'unit': 'PT'},
                    'marginRight':  {'magnitude': 42.52, 'unit': 'PT'},
                },
                'fields': 'pageSize,marginTop,marginBottom,marginLeft,marginRight'
            }
        },
        # Основной текст — Times New Roman 12pt
        {
            'updateTextStyle': {
                'range': {'startIndex': 1, 'endIndex': end_idx},
                'textStyle': {
                    'weightedFontFamily': {'fontFamily': 'Times New Roman'},
                    'fontSize': {'magnitude': 11, 'unit': 'PT'},
                },
                'fields': 'weightedFontFamily,fontSize'
            }
        },
        # Межстрочный интервал 1.15
        {
            'updateParagraphStyle': {
                'range': {'startIndex': 1, 'endIndex': end_idx},
                'paragraphStyle': {
                    'lineSpacing': 115,
                    'spaceAbove': {'magnitude': 0, 'unit': 'PT'},
                    'spaceBelow': {'magnitude': 0, 'unit': 'PT'},
                },
                'fields': 'lineSpacing,spaceAbove,spaceBelow'
            }
        },
    ]

    # Заголовки — жирные, 14pt, по центру
    for elem in body_content:
        if 'paragraph' not in elem:
            continue
        para = elem['paragraph']
        text = ''.join(
            e.get('textRun', {}).get('content', '')
            for e in para.get('elements', [])
        ).strip()
        is_title = text == 'АРХИТЕКТУРА POSTER И ЛОГИКА P&L — РОМАШКА'
        is_section = text.startswith('РАЗДЕЛ ') or text.startswith('ТЕХНИЧЕСКИЙ РАЗДЕЛ')
        is_separator = text.startswith('━')
        if (is_title or is_section) and not is_separator:
            s_idx = elem['startIndex']
            e_idx = elem['endIndex']
            font_size = 16 if is_title else 13
            reqs += [
                {
                    'updateTextStyle': {
                        'range': {'startIndex': s_idx, 'endIndex': e_idx - 1},
                        'textStyle': {
                            'bold': True,
                            'fontSize': {'magnitude': font_size, 'unit': 'PT'},
                        },
                        'fields': 'bold,fontSize'
                    }
                },
                {
                    'updateParagraphStyle': {
                        'range': {'startIndex': s_idx, 'endIndex': e_idx},
                        'paragraphStyle': {
                            'alignment': 'CENTER',
                            'spaceAbove': {'magnitude': 10, 'unit': 'PT'},
                            'spaceBelow': {'magnitude': 4, 'unit': 'PT'},
                        },
                        'fields': 'alignment,spaceAbove,spaceBelow'
                    }
                },
            ]

    r2 = s.post(
        f'https://docs.googleapis.com/v1/documents/{doc_id}:batchUpdate',
        json={'requests': reqs}, timeout=30
    )
    return r2.status_code == 200

def main():
    title = 'Архитектура Poster и логика P&L — Ромашка 2026'
    print(f'Создаю документ: «{title}»')

    s = get_session()

    doc_id = create_doc_in_drive(s, title)
    print(f'✅ Документ создан: https://docs.google.com/document/d/{doc_id}')

    ok = insert_text(s, doc_id, CONTENT)
    print(f'{"✅" if ok else "❌"} Текст вставлен')

    ok2 = apply_styles(s, doc_id)
    print(f'{"✅" if ok2 else "❌"} Стили применены')

    print(f'\n📄 Открыть: https://docs.google.com/document/d/{doc_id}/edit')

if __name__ == '__main__':
    main()
